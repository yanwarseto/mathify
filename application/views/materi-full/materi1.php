            <h2 style="color: black; text-align:center; margin-top:4vh">Jenis dan Sifat Segiempat</h2>
            <p style="margin-top: 5vh; text-align:justify;">Ketika mendengar tentang bangun datar kita pasti terlingat
                pada persegi dan persegi panjang. Namun, bangun datar segi empat memiliki berbagai jenis dengan sifat
                yang berbeda-beda.segi empat adalah poligon yang memiliki empat sisi, empat sudut, dan empat simpul.
                Adapun, segi empat memiliki dua diagonal dengan jumlah semua sudut dalamnya adalah 360°.
                Pada kesempatan kali ini <b>Mathify</b> ingin menjelaskan jenis-jenis segiempat.</p>

            <h5 style="margin-top:2vh; color:black;"> 1. Kotak atau Persegi</h5>
            <div class="center-image">
                <img src="<?= base_url('assets/') ?>img /segiempat/persegi.jpg" alt="">
            </div>
            <p style="margin-top: 2vh; text-align:justify;">Persegi adalah jenis bangun segi empat yang sisi-sisinya
                sama panjang dan membentuk sudut siku-siku (90o). Contoh benda yang berbentuk persegi, di antaranya
                papan catur, kertas origami, roti tawar, lantai keramik, dan lain sebagainya.</p>

            <h5 style="margin-top:2vh; color:black;"> 2. Persegi Panjang</h5>
            <div class="center-image">
                <img src="<?= base_url('assets/') ?>img /segiempat/persegi-panjang.jpg" alt="">
            </div>
            <p style="margin-top: 2vh; text-align:justify;">Persegi panjang adalah jenis bangun segi empat dimana
                sisi-sisi yang berhadapan saling sejajar dan punya panjang yang sama. Contoh benda yang berbentuk
                persegi panjang, di antaranya lapangan sepak bola, lapangan basket, lapangan bulu tangkis, papan tulis,
                pintu, permukaan meja, kertas HVS, dan lain sebagainya.</p>

            <h5 style="margin-top:2vh; color:black;"> 3. Trapesium</h5>
            <div class="center-image">
                <img src="<?= base_url('assets/') ?>img /segiempat/trapesium.jpg" alt="">
            </div>
            <p style="margin-top: 2vh; text-align:justify;">Secara umum, trapesium adalah jenis bangun segi empat yang
                hanya memiliki sepasang sisi sejajar yang saling berhadapan. Trapesium terbagi menjadi 3 jenis, yaitu
                trapesium sama kaki, trapesium siku-siku, dan trapesium sembarang. Contoh benda yang berbentuk
                trapesium, di antaranya atap rumah, permukaan samping perahu, permukaan samping bak mandi, dan lain
                sebagainya.</p>

            <h5 style="margin-top:2vh; color:black;"> 4. Belah Ketupat</h5>
